                'norm_kind', 'peak', ...
                'crop_pixels', size(im, 1), ...
                'results_dir', testCase.results_dir);
            fprintf(['  3b Elt 8->9 sphere: max=%.3e rms=%.3e ' ...
                     'Δcom=(%+d,%+d) px\n'], ...
                m.max_abs, m.rms_abs, m.dx_pix, m.dy_pix);
            testCase.verifyLessThan(m.max_abs, 0.1, sprintf( ...
                'max |a-b| = %.3e peak-norm > 0.1', m.max_abs));
        end

